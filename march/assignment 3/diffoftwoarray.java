public class diffoftwoarray {
    public static void main(String[] args){
        int a[]={80,70,30};
        int b[]={50,60,70};
        
        int c[]= new int[3];
        for(int i=0;i<a[i];i++){
            c[i]=a[i]-b[i];
            System.out.println(c[i]);
        }
        
        
    }
}
