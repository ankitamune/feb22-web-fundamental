public class bitwise {
    public static void main(String args[]){
        int a=7;
        int b=5;
        System.out.println(a&b);
        System.out.println(a|b);
        System.out.println(a^b);
        int a1=17;
        int b2=9;
        System.out.println(a1&b2); 
        System.out.println(a1|b2);
        System.out.println(a1^b2);
        int a3 = 91, b3=56;
        System.out.println(a3&b3); 
        System.out.println(a3|b3);
        System.out.println(a3^b3);
        
    }
}